package week1.casestudy;

public class FibonacciSeries {

	public static void main(String[] args) {
		int terms=8;
		int first=0;
		int second=1;
		for (int i= 1;  i<=terms ; i++) {
			System.out.println(first + "");
		int next = first + second;
				first=second;
		second=next;
		}
		

	}

}
